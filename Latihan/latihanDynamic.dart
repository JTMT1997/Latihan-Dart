void main() {
  // Deklarasi variabel dynamic
  dynamic data;

  // Mengisi dengan integer
  data = 100;
  print("Data sekarang (int): $data");

  // Mengisi dengan string
  data = "Halo, ini string";
  print("Data sekarang (String): $data");

  // Mengisi dengan double
  data = 3.14;
  print("Data sekarang (double): $data");

  // Mengisi dengan boolean
  data = true;
  print("Data sekarang (bool): $data");

  // Mengisi dengan List
  data = [1, 2, 3];
  print("Data sekarang (List): $data");
}

